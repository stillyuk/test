package com.hibtest1.entity;

public class Phrase{
	private String pid;
	private String pcontent;
	private String pword;
	private String psound;
	private String pstate;
	private String pmean;
	private String panswer;

	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getPcontent() {
		return pcontent;
	}
	public void setPcontent(String pcontent) {
		this.pcontent = pcontent;
	}
	public String getPword() {
		return pword;
	}
	public void setPword(String pword) {
		this.pword = pword;
	}
	public String getPsound() {
		return psound;
	}
	public void setPsound(String psound) {
		this.psound = psound;
	}
	public String getPstate() {
		return pstate;
	}
	public void setPstate(String pstate) {
		this.pstate = pstate;
	}
	public String getPmean() {
		return pmean;
	}
	public void setPmean(String pmean) {
		this.pmean = pmean;
	}
	public String getPanswer() {
		return panswer;
	}
	public void setPanswer(String panswer) {
		this.panswer = panswer;
	}
	

}
